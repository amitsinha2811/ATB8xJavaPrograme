package sept.ex16092024;

public class Lab014_Unary_Operator {
    public static void main(String[] args) {
        String First_name = "Ashmit";
        String Last_name = "Sinha";
        // In case of strings + operator is called Concatination
        int a =10;
        int b = 10;
        System.out.println(First_name + Last_name + a + b);
        System.out.println(a + b + First_name + Last_name);
    }
}
