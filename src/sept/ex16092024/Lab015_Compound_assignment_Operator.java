package sept.ex16092024;

public class Lab015_Compound_assignment_Operator {
    public static void main(String[] args) {
        // += , -=, *= , /= are called compound operator
        int a= 10;
        a += 20;
        System.out.println(a);
        a -=5;
        System.out.println(a);
        a *=10;
        System.out.println(a);
        a /=2;
        System.out.println(a);

    }
}
